package com.portfolio.rosalucciarini.Servidor;

import com.portfolio.rosalucciarini.Entidad.Educacion;
import com.portfolio.rosalucciarini.Repositorio.REducacion;
import java.util.List;
import java.util.Optional;
import javax.transaction.Transactional;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
@Transactional
public class ServEducacion {
    @Autowired
    REducacion rEducacion;
    
    public List<Educacion> list(){
        return rEducacion.findAll();
    }
    
    public Optional<Educacion> getOne(int id){
        return rEducacion.findById(id);
    }
    
    public Optional<Educacion> getByNombrE(String nombrE){
        return rEducacion.findByNombrE(nombrE);
    }
    
    public void save(Educacion educacion){
        rEducacion.save(educacion);
    }
    
    public void delete(int id){
        rEducacion.deleteById(id);
    }
    
    public boolean existsById(int id){
        return rEducacion.existsById(id);
    }
    
     
    public boolean existsByNombrE(String nombrE){
        return rEducacion.existsByNombrE(nombrE);    
    }
}
   
